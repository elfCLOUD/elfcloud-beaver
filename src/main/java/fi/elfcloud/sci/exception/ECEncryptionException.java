package fi.elfcloud.sci.exception;

public class ECEncryptionException extends Exception {
	private static final long serialVersionUID = 1472228041803856452L;
	private int id;
	private String message;
	
	public ECEncryptionException(int id, String message) {
		this.setMessage(message);
		this.setId(id);
	}
	
	public ECEncryptionException() {
		
	}
	
	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}
}
